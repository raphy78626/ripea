
CREATE OR REPLACE TRIGGER IPA_ACLCLA_IDGEN_TRG
BEFORE INSERT ON IPA_ACL_CLASS
FOR EACH ROW
  BEGIN
    SELECT IPA_ACL_CLASS_SEQ.NEXTVAL INTO :new.id FROM dual;
  END;
/
SHOW ERRORS;
 
